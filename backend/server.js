app.get('/api/dashboard/overview', verifyToken, async (req, res) => {
  try {
    const alertCount = await Alert.countDocuments();
    const trafficStats = await TrafficLog.aggregate([
      { $group: { _id: null, totalSent: { $sum: "$bytes_sent" }, totalReceived: { $sum: "$bytes_received" } } }
    ]);
    const totalBytes = trafficStats.length > 0 ? (trafficStats[0].totalSent + trafficStats[0].totalReceived) : 0;
    res.json({ totalTrafficGb: (totalBytes / 1024).toFixed(2), guardDutyAlerts: alertCount, lookoutAnomalies: Math.floor(alertCount / 2) });
  } catch (error) { res.status(500).json({ error: error.message }); }
});

