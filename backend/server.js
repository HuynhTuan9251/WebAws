app.get('/api/traffic/top-ips', verifyToken, async (req, res) => {
  try {
    const { protocol } = req.query;
    const matchStage = protocol && protocol !== 'ALL' ? { $match: { protocol } } : { $match: {} };
    
    const topIps = await TrafficLog.aggregate([
      matchStage,
      { $group: { _id: "$source_ip", total_bytes: { $sum: "$bytes_sent" } } },
      { $sort: { total_bytes: -1 } },
      { $limit: 10 }
    ]);
    
    const formatted = topIps.map(item => ({ ip: item._id, bytes: item.total_bytes }));
    res.json(formatted);
  } catch(error) {
    res.status(500).json({ error: error.message });
  }
});


