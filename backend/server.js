// Yêu cầu Role phải là SecOps hoặc SystemAdmin mới được đổi trạng thái cảnh báo
app.put('/api/security/alerts/:id/status', verifyToken, checkRole(['SecOps', 'SystemAdmin']), async (req, res) => {
  try {
    const { status } = req.body;
    const user = req.user.username; // Lấy từ Token
    const alert = await Alert.findById(req.params.id);
    if(!alert) return res.status(404).json({ error: "Alert not found" });
    
    alert.status = status;
    alert.audit_logs.push({ action: `Status changed to ${status}`, user: user });
    await alert.save();
    
    res.json(alert);
  } catch (error) { res.status(500).json({ error: error.message }); }
});

